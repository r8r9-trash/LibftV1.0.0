#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write(fd, '-', 1);
		ft_putchar(fd, '2', 1);
		n = 147483648;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar(fd, '-', 1);
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n >= 0 && n < 10)
	{
		write(fd, n + '0', 1);
	}
}
