#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
    int     i;
    int     k;
    char    *p;
    
    i = 0;
    k = c;
    p = (char*)s;
    while (p[i] != '\0')
    {
        if (p[i] == k )
            return (&p[i]);
        i++;
    }
    return (NULL);
}
